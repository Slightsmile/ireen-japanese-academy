/* ===========================
   COUNTER ANIMATION
=========================== */
function animateCounter(el) {
  const target = parseInt(el.getAttribute('data-target'));
  const duration = 2000;
  const start = performance.now();

  function update(time) {
    const elapsed = time - start;
    const progress = Math.min(elapsed / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    el.textContent = Math.floor(eased * target);
    if (progress < 1) requestAnimationFrame(update);
    else el.textContent = target;
  }
  requestAnimationFrame(update);
}

const counterObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.querySelectorAll('.counter').forEach(animateCounter);
      counterObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.3 });

document.querySelectorAll('#hero, #stats').forEach(el => {
  if (el) counterObserver.observe(el);
});

/* ===========================
   TESTIMONIALS SLIDER
=========================== */
const track = document.getElementById('testimonialsTrack');
const dots = document.querySelectorAll('.slider-dot');
let currentSlide = 0;
const totalCards = track.children.length;

function getSlidesPerView() {
  if (window.innerWidth <= 768) return 1;
  if (window.innerWidth <= 1024) return 2;
  return 3;
}

function updateSlider() {
  const spv = getSlidesPerView();
  const maxSlide = totalCards - spv;
  if (currentSlide > maxSlide) currentSlide = maxSlide;
  if (currentSlide < 0) currentSlide = 0;
  const cardWidth = track.children[0].offsetWidth + 28;
  track.style.transform = `translateX(-${currentSlide * cardWidth}px)`;
  dots.forEach((dot, i) => dot.classList.toggle('active', i === currentSlide));
}

document.getElementById('slidePrev').addEventListener('click', () => {
  currentSlide = Math.max(0, currentSlide - 1);
  updateSlider();
});

document.getElementById('slideNext').addEventListener('click', () => {
  currentSlide = Math.min(totalCards - getSlidesPerView(), currentSlide + 1);
  updateSlider();
});

dots.forEach(dot => {
  dot.addEventListener('click', () => {
    currentSlide = parseInt(dot.getAttribute('data-index'));
    updateSlider();
  });
});

window.addEventListener('resize', updateSlider);

setInterval(() => {
  currentSlide = currentSlide >= totalCards - getSlidesPerView() ? 0 : currentSlide + 1;
  updateSlider();
}, 5000);

